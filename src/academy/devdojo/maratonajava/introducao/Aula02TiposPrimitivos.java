package academy.devdojo.maratonajava.introducao;

/**
 * Tipos primitivos
 */

public class Aula02TiposPrimitivos {
    public static void main(String[] args) {

        byte numberByte = 127;
        short numberShort = 32767;
        int numberInt = 2147483647;
        long numberLong = 9223372036854775807L;
        float numberFloat = 3.14F;
        double numberDouble = 3.14159265358979323846;
        boolean resultBoolean = true;
        char textChar = 'a';

        String nome = "Leonardo";

        System.out.println("O número do byte é: " + numberByte);
        System.out.println("O número do short é: " + numberShort);
        System.out.println("O número do int é: " + numberInt);
        System.out.println("O número do long é: " + numberLong);
        System.out.println("O número do flotat é: " + numberFloat);
        System.out.println("O número do double é: " + numberDouble);
        System.out.println("O resultado do booltean é: " + resultBoolean);
        System.out.println("O texto do char é: " + textChar);
        System.out.println("O nome da Stringe é: " + nome);

    }
}
